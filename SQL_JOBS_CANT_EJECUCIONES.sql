SELECT SUBSTR(TAB1.FECHA,4,2) MM, SUBSTR(TAB1.FECHA,7,4) YY, JOBS, ROUND(AVG(CANTIDAD),0)  CANTIDAD  
FROM
(SELECT To_Char(To_Date(start_ts,'DD-MM-YYYY HH24:MI:SS'),'DD/MM/YYYY') FECHA, 
JOBS,  
COUNT(*) CANTIDAD
FROM AS400JOBS
where  status in ('C') and
JOBS IN 
('CCECDSCD7',
'CCECDSCD8',
'SW94001P')
GROUP BY To_Char(To_Date(start_ts,'DD-MM-YYYY HH24:MI:SS'),'DD/MM/YYYY'), JOBS
ORDER BY To_Char(To_Date(start_ts,'DD-MM-YYYY HH24:MI:SS'),'DD/MM/YYYY'),JOBS) TAB1
GROUP BY SUBSTR(TAB1.FECHA,4,2),SUBSTR(TAB1.FECHA,7,4),JOBS
ORDER BY SUBSTR(TAB1.FECHA,4,2),SUBSTR(TAB1.FECHA,7,4)
;

SELECT To_Char(To_Date(start_ts,'DD-MM-YYYY HH24:MI:SS'),'DD/MM/YYYY') FECHA, 
CASE WHEN SUBSTR(JOBS,1,2)='SC' THEN 'SISTEMA INTEGRAL DE COBROS' ELSE 'SISTEMA INTEGRAL DE PAGOS' END SISTEMA,  
COUNT(*) CANTIDAD
FROM AS400JOBS
where SUBSTR(JOBS,1,2) in ('SC','SP') and status in ('C') and
JOBS IN ('SC0001CL','SC0002M','SC0010','SC0111CL','SC0113','SC0141CL','SC0143CL','SC0152','SC0160CLB','SC0201CL','SP0008CL','SP0013CL','SP0018CL','SP0061CL','SP0062CL','SP0082CL','SP0096CL','SP0099CL','SP0140CL','SP0142CL','SP0150CL','SP0172CL','SP0190','SP9000CL','SP9001CL','SP9012CL')
GROUP BY To_Char(To_Date(start_ts,'DD-MM-YYYY HH24:MI:SS'),'DD/MM/YYYY'), SUBSTR(JOBS,1,2)
ORDER BY To_Char(To_Date(start_ts,'DD-MM-YYYY HH24:MI:SS'),'DD/MM/YYYY'),SUBSTR(JOBS,1,2);