SELECT SUBSTR(TAB1.FECHA,1,2) DD, SUBSTR(TAB1.FECHA,4,2) MM, SUBSTR(TAB1.FECHA,7,4) YY, SISTEMA, ROUND(AVG(CANTIDAD),0)  CANTIDAD , 
CASE WHEN SISTEMA='SISTEMA INTEGRAL DE COBROS' THEN ROUND(14 - AVG(CANTIDAD),0) ELSE ROUND(74 - AVG(CANTIDAD),0) END DIF FROM
(SELECT To_Char(To_Date(start_ts,'DD-MM-YYYY HH24:MI:SS'),'DD/MM/YYYY') FECHA, 
CASE WHEN SUBSTR(JOBS,1,2)='SC' THEN 'SISTEMA INTEGRAL DE COBROS' ELSE 'SISTEMA INTEGRAL DE PAGOS' END SISTEMA,  
COUNT(*) CANTIDAD
FROM AS400JOBS
where SUBSTR(JOBS,1,2) in ('SC','SP') and status in ('C') and
JOBS IN ('SC0001CL','SC0002M','SC0010','SC0111CL','SC0113','SC0141CL','SC0143CL','SC0152','SC0160CLB','SC0201CL','SP0008CL','SP0013CL','SP0018CL','SP0061CL','SP0062CL','SP0082CL','SP0096CL','SP0099CL','SP0140CL','SP0142CL','SP0150CL','SP0172CL','SP0190','SP9000CL','SP9001CL','SP9012CL')
GROUP BY To_Char(To_Date(start_ts,'DD-MM-YYYY HH24:MI:SS'),'DD/MM/YYYY'), SUBSTR(JOBS,1,2)
ORDER BY To_Char(To_Date(start_ts,'DD-MM-YYYY HH24:MI:SS'),'DD/MM/YYYY'),SUBSTR(JOBS,1,2)) TAB1
GROUP BY SUBSTR(TAB1.FECHA,1,2),SUBSTR(TAB1.FECHA,4,2),SUBSTR(TAB1.FECHA,7,4),SISTEMA
ORDER BY SUBSTR(TAB1.FECHA,1,2),SUBSTR(TAB1.FECHA,4,2),SUBSTR(TAB1.FECHA,7,4)
;

